public class ExerciseEight {
    public static void exerciseEight() {
        for (int i = 1; i < 10; i++) {
            for (int j = 1; j < 10; j++) {
                System.out.print(i * j + "\t");
            }
            System.out.print("\n"); //System.out.println(); Does exactly the same
        }
    }
}
